using System;

namespace CharAndString
{
    public class CharAndString
    {
        public static void Main(string[] args)
        {
            string a = "Software University";
            char b = 'B';
            char c = 'y';
            char d = 'e';
            string e = "I love programming";
            Console.WriteLine("{0}\n{1}\n{2}\n{3}\n{4}\n", a, b, c, d, e);
        }
    }
}
